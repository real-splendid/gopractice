package leetcode

import (
	"testing"
)

func TestIsValidSudoku(t *testing.T) {
	check := func(want, got bool, msg string) {
		if want != got {
			t.Errorf("\nfailed test %v\nwant %v \ngot  %v", msg, want, got)
		}
	}
	b := func(v int) byte { return byte(v) }

	check(
		true,
		isValidSudoku([][]byte{
			{b('5'), b('3'), b('.'), b('.'), b('7'), b('.'), b('.'), b('.'), b('.')},
			{b('6'), b('.'), b('.'), b('1'), b('9'), b('5'), b('.'), b('.'), b('.')},
			{b('.'), b('9'), b('8'), b('.'), b('.'), b('.'), b('.'), b('6'), b('.')},
			{b('8'), b('.'), b('.'), b('.'), b('6'), b('.'), b('.'), b('.'), b('3')},
			{b('4'), b('.'), b('.'), b('8'), b('.'), b('3'), b('.'), b('.'), b('1')},
			{b('7'), b('.'), b('.'), b('.'), b('2'), b('.'), b('.'), b('.'), b('6')},
			{b('.'), b('6'), b('.'), b('.'), b('.'), b('.'), b('2'), b('8'), b('.')},
			{b('.'), b('.'), b('.'), b('4'), b('1'), b('9'), b('.'), b('.'), b('5')},
			{b('.'), b('.'), b('.'), b('.'), b('8'), b('.'), b('.'), b('7'), b('9')},
		}),
		"one line 17",
	)
	check(
		false,
		isValidSudoku([][]byte{
			// {b('8'), b('3'), b('.'), b('.'), b('7'), b('.'), b('.'), b('.'), b('.')},
			{b('5'), b('3'), b('9'), b('.'), b('7'), b('.'), b('.'), b('.'), b('.')},
			{b('6'), b('.'), b('.'), b('1'), b('9'), b('5'), b('.'), b('.'), b('.')},
			{b('.'), b('9'), b('8'), b('.'), b('.'), b('.'), b('.'), b('6'), b('.')},
			{b('8'), b('.'), b('.'), b('.'), b('6'), b('.'), b('.'), b('.'), b('3')},
			{b('4'), b('.'), b('.'), b('8'), b('.'), b('3'), b('.'), b('.'), b('1')},
			{b('7'), b('.'), b('.'), b('.'), b('2'), b('.'), b('.'), b('.'), b('6')},
			{b('.'), b('6'), b('.'), b('.'), b('.'), b('.'), b('2'), b('8'), b('.')},
			{b('.'), b('.'), b('.'), b('4'), b('1'), b('9'), b('.'), b('.'), b('5')},
			{b('.'), b('.'), b('.'), b('.'), b('8'), b('.'), b('.'), b('7'), b('9')},
		}),
		"two line 32",
	)
}